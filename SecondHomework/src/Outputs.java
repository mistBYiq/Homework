public class Outputs {
    public static void showArrNum(int[] a) {
        for (int number : a) {
            System.out.print(number + "  ");
        }
        System.out.println();
    }

    public static void showArrStr(String[] s) {
        for (String strings: s) {
            System.out.print(strings + "  ");
        }
        System.out.println();
    }
}
